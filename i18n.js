function translatePage() {
  const lang = localStorage.getItem("language") || "th";
  const currentTranslations = translations[lang];
  
  if (!currentTranslations) return;
  
  // ‡πÅ‡∏õ‡∏•‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡∏ó‡∏±‡πà‡∏ß‡πÑ‡∏õ - ‡πÉ‡∏ä‡πâ DocumentFragment ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏õ‡∏£‡∏∞‡∏™‡∏¥‡∏ó‡∏ò‡∏¥‡∏†‡∏≤‡∏û
  const elementsToTranslate = document.querySelectorAll("[data-i18n]");
  elementsToTranslate.forEach(el => {
    const key = el.getAttribute("data-i18n");
    const translation = currentTranslations[key];
    if (translation) {
      el.textContent = translation;
    }
  });

  // ‡πÅ‡∏õ‡∏• placeholder
  const placeholderElements = document.querySelectorAll("[data-i18n-placeholder]");
  placeholderElements.forEach(el => {
    const key = el.getAttribute("data-i18n-placeholder");
    const translation = currentTranslations[key];
    if (translation) {
      el.setAttribute("placeholder", translation);
    }
  });

  // ‡πÅ‡∏õ‡∏• title attributes
  const titleElements = document.querySelectorAll("[data-i18n-title]");
  titleElements.forEach(el => {
    const key = el.getAttribute("data-i18n-title");
    const translation = currentTranslations[key];
    if (translation) {
      el.setAttribute("title", translation);
    }
  });

  // ‡πÅ‡∏õ‡∏• aria-label attributes
  const ariaElements = document.querySelectorAll("[data-i18n-aria]");
  ariaElements.forEach(el => {
    const key = el.getAttribute("data-i18n-aria");
    const translation = currentTranslations[key];
    if (translation) {
      el.setAttribute("aria-label", translation);
    }
  });

  // ‡πÅ‡∏õ‡∏• option ‡πÉ‡∏ô select
  const optionElements = document.querySelectorAll("option[data-i18n]");
  optionElements.forEach(el => {
    const key = el.getAttribute("data-i18n");
    const translation = currentTranslations[key];
    if (translation) {
      el.textContent = translation;
    }
  });

  // ‡∏≠‡∏±‡∏û‡πÄ‡∏î‡∏ó language selector
  const languageSelects = document.querySelectorAll("#languageSelect");
  languageSelects.forEach(select => {
    if (select) select.value = lang;
  });

  // ‡∏≠‡∏±‡∏û‡πÄ‡∏î‡∏ó‡∏†‡∏≤‡∏©‡∏≤‡∏Ç‡∏≠‡∏á‡∏´‡∏ô‡πâ‡∏≤
  document.documentElement.lang = lang === 'th' ? 'th' : 'en';
}

